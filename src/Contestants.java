public class Contestants {

    String divaName;
    int scoreCard;

    public Contestants (String name) {

        this.divaName = name;
        this.scoreCard = scoreCard;


    }

    public void setDivaName(String divaName) {
        this.divaName = divaName;
    }

    public void setScoreCard(int scoreCard) {
        this.scoreCard = scoreCard;
    }

    public int getScoreCard() {
        return scoreCard;
    }

    public String getDivaName() {
        return divaName;
    }

}
